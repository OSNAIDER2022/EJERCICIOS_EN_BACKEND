package com.amsterdam.clinicaOdontologica.model;

public class Domicilio {
    //ATRIBUTOS
    private Integer id;
    private String calle;
    private String numero;
    private String localidad;
    private String provincia;

    //CONSTRUCTOR:
    //se deben crear dos constructores: uno con el id y el otro sin el id:
    public Domicilio(Integer id, String calle, String numero, String localidad, String provincia) {
        this.id = id;
        this.calle = calle;
        this.numero = numero;
        this.localidad = localidad;
        this.provincia = provincia;
    }
    public Domicilio(String calle, String numero, String localidad, String provincia) {
        this.calle = calle;
        this.numero = numero;
        this.localidad = localidad;
        this.provincia = provincia;
    }
    //para que en futuras peticiones POST se construya el json de respuesta se debe establecer el constructor por defecto:
    public Domicilio(){ }
    //GETTERS AND SETTERS:

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCalle() {
        return calle;
    }

    public void setCalle(String calle) {
        this.calle = calle;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getLocalidad() {
        return localidad;
    }

    public void setLocalidad(String localidad) {
        this.localidad = localidad;
    }

    public String getProvincia() {
        return provincia;
    }

    public void setProvincia(String provincia) {
        this.provincia = provincia;
    }
}
