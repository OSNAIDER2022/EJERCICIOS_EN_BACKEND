public class Main {
    public static void main(String[] args) {
        //ARITHMETICS OPERATORS:
        //Los operadores aritméticos son:

        int a = 23;
        int b = 45;

        //De Suma:
        System.out.println("La suma de A mas B, es: "+ (a + b));

        //de resta:
        System.out.println("La resta de A menos B, es: "+ (a - b));

        //de multiplicación:
        System.out.println("La multiplicación de A por B, es: "+ (a * b));

        //de división:
        System.out.println("La división de A entre B, es: "+ (a / b));

        //de residuo:
        System.out.println("El residuo de A y B, es: "+ (a % b));
    }
}